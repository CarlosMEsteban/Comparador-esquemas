package client;

public class ParametroOT 
{
  protected String nombre = "";
  protected String valor = "";
  public ParametroOT()
  {
  }


  public void setNombre(String nombre)
  {
    this.nombre = nombre;
  }


  public String getNombre()
  {
    return nombre;
  }


  public void setValor(String valor)
  {
    this.valor = valor;
  }


  public String getValor()
  {
    return valor;
  }
}