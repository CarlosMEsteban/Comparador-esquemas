package client;

import java.util.ArrayList;

public class RolOT
{
  protected String cGrupoId = "";
  protected ArrayList lProcesos = new ArrayList();
  
  public RolOT()
  {
  }

  public void setCGrupoId(String cGrupoId)
  {
    this.cGrupoId = cGrupoId;
  }

  public String getCGrupoId()
  {
    return cGrupoId;
  }


  public void setLProcesos(ArrayList lProcesos)
  {
    this.lProcesos = lProcesos;
  }

  public ArrayList getLProcesos()
  {
    return lProcesos;
  }
}
